# == Equal
# != Not equal
# > Greater than
# < Less than
# >= Greater than or Equal to
# <= Less than or Equal to
print(10 == 10)
print(10 == 11)
print(10 > 13)
print(10 > 9)
print(10 <= 9)
print(10 <= 10)
print(10 >= 10)
